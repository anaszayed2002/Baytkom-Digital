let currentLang = localStorage.getItem("baytkom_lang") || "ar";

function setLanguage(lang) {
  currentLang = lang;
  localStorage.setItem("baytkom_lang", lang);
  
  // Set HTML attributes
  document.documentElement.lang = lang;
  document.documentElement.dir = lang === "ar" ? "rtl" : "ltr";
  
  // Translate static data-i18n items
  const elements = document.querySelectorAll("[data-i18n]");
  elements.forEach(el => {
    const key = el.getAttribute("data-i18n");
    if (translations[lang] && translations[lang][key]) {
      if (el.tagName === "INPUT" || el.tagName === "TEXTAREA") {
        el.placeholder = translations[lang][key];
      } else {
        el.textContent = translations[lang][key];
      }
    }
  });

  // Update layout components (like specific buttons that need text/icon combinations)
  updateDynamicI18nElements(lang);
  
  // Reload global AI videos on language change
  loadGlobalAiVideos();
  
  // Dispatch event for other scripts (e.g. portfolio.js and project.js) to re-render in correct lang
  window.dispatchEvent(new CustomEvent("langchanged", { detail: { lang } }));
}

function updateDynamicI18nElements(lang) {
  // Update Language Button Text
  const langSwitchBtn = document.querySelector(".lang-switch span");
  if (langSwitchBtn) {
    langSwitchBtn.textContent = translations[lang]["lang_btn"];
  }
}

// Toggle Language Handler
function toggleLanguage() {
  const nextLang = currentLang === "ar" ? "en" : "ar";
  setLanguage(nextLang);
}

