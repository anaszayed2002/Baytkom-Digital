function loadGlobalAiVideos() {
  const grid = document.getElementById("global-ai-videos-grid");
  if (!grid) return;
  grid.innerHTML = "";
  
  let totalVideosLoaded = 0;
  const videosSet = new Set(); // Prevent duplicate URLs
  
  const appendVideoToGrid = (url, title) => {
    if (videosSet.has(url)) return;
    videosSet.add(url);
    
    totalVideosLoaded++;
    const card = document.createElement("div");
    card.className = "video-card glass-panel reveal";
    card.innerHTML = `
      <video controls preload="metadata">
        <source src="${url}" type="video/mp4">
        Your browser does not support the video tag.
      </video>
      <div class="video-info" style="padding: 1rem 1.5rem; border-top: 1px solid var(--border-glass);">
        <h4 style="font-size: 0.95rem; font-weight: 700; color: var(--text-white);">${title}</h4>
      </div>
    `;
    grid.appendChild(card);
    
    if (window.revealObserver) {
      window.revealObserver.observe(card);
    } else {
      card.classList.add("active");
    }
  };

  const formats = ["mp4", "webm", "mov"];

  // 1. Fetch custom videos list first from data/videos.json
  fetch("data/videos.json")
    .then(res => {
      if (res.ok) return res.json();
      return [];
    })
    .then(customVideos => {
      if (Array.isArray(customVideos)) {
        customVideos.forEach((videoName, idx) => {
          const videoUrl = `assets/videos/${videoName}`;
          const currentLang = document.documentElement.lang || "ar";
          const title = currentLang === "ar" ? `فيديو إعلاني رقم ${idx + 1}` : `AI Advertisement Spot ${idx + 1}`;
          appendVideoToGrid(videoUrl, title);
        });
      }
    })
    .catch(() => {})
    .finally(() => {
      // 2. Probe global assets videos numerically (1.mp4, 2.mp4, etc.)
      for (let i = 1; i <= 20; i++) {
        probeSingleGlobalVideoFile(i, formats, (url) => {
          const currentLang = document.documentElement.lang || "ar";
          const title = currentLang === "ar" ? `فيديو إعلاني رقم ${i}` : `AI Advertisement Spot ${i}`;
          appendVideoToGrid(url, title);
        });
      }
    });

  // 3. Fetch and load all project videos
  fetch("data/projects.json")
    .then(res => {
      if (!res.ok) throw new Error();
      return res.json();
    })
    .then(projects => {
      projects.forEach(project => {
        const currentLang = document.documentElement.lang || "ar";
        const projectTitle = project.name[currentLang] || project.name.en;
        
        // If explicit list exists in projects.json
        if (project.videosList && Array.isArray(project.videosList) && project.videosList.length > 0) {
          project.videosList.forEach((videoName, idx) => {
            const videoUrl = `projects/${project.id}/videos/${videoName}`;
            appendVideoToGrid(videoUrl, `${projectTitle} - Video ${idx + 1}`);
          });
        } else {
          // Probe project folder numerically
          for (let v = 1; v <= 10; v++) {
            probeSingleProjectVideoFile(project.id, v, formats, (url) => {
              appendVideoToGrid(url, `${projectTitle} - Video ${v}`);
            });
          }
        }
      });
    })
    .catch(() => {});
}

function probeSingleGlobalVideoFile(index, extensions, onSuccess) {
  let extIdx = 0;
  const tryNext = () => {
    if (extIdx >= extensions.length) return;
    const ext = extensions[extIdx];
    const url = `assets/videos/${index}.${ext}`;
    
    const controller = new AbortController();
    fetch(url, { signal: controller.signal })
      .then(res => {
        if (res.ok) {
          onSuccess(url);
        } else {
          extIdx++;
          tryNext();
        }
        controller.abort();
      })
      .catch(err => {
        if (err.name === 'AbortError') {
          // Aborted, this is expected
        } else {
          extIdx++;
          tryNext();
        }
      });
  };
  tryNext();
}

